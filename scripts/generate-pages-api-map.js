/**
 * 扫描 pages/api 下所有 .ts/.tsx（排除 [...all].ts），生成 pathKey -> import 的映射。
 * 运行: node scripts/generate-pages-api-map.js
 * 输出: src/lib/pagesApiHandlerMap.generated.ts
 */
const fs = require('fs');
const path = require('path');

const apiDir = path.join(__dirname, '..', 'pages', 'api');
const outFile = path.join(__dirname, '..', 'src', 'lib', 'pagesApiHandlerMap.generated.ts');

function walk(dir, base = '') {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const keys = [];
  for (const e of entries) {
    const rel = base ? `${base}/${e.name}` : e.name;
    if (e.name.startsWith('[') && e.name.includes('...')) continue; // skip [...all].ts
    if (e.isDirectory()) {
      keys.push(...walk(path.join(dir, e.name), rel));
    } else if (e.isFile() && /\.(ts|tsx)$/.test(e.name)) {
      let key = rel.replace(/\.(tsx?)$/, '').replace(/\/index$/, ''); // list.ts -> list, .../index.ts -> ...
      if (key) keys.push(key);
    }
  }
  return keys;
}

const keys = walk(apiDir);
const lines = [
  '// AUTO-GENERATED by scripts/generate-pages-api-map.js - do not edit by hand',
  'export type PagesApiPathKey = ' + keys.map((k) => `'${k}'`).join(' | ') + ';',
  '',
  'export async function loadPagesApiHandler(pathKey: string): Promise<{ default: (req: any, res: any) => void | Promise<void> } | null> {',
  '  switch (pathKey) {',
  ...keys.map((k) => `    case '${k}': return import('@/pages/api/${k}');`),
  '    default: return null;',
  '  }',
  '}',
  '',
];

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, lines.join('\n'), 'utf8');
console.log('Wrote', keys.length, 'paths to', outFile);
